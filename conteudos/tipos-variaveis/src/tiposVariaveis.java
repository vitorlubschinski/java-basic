public class tiposVariaveis {

    public static void main(String[] args) {
        byte idade = 123;
        short ano = 2021;
        int cep = 21070333; // se começar com zero, talvez tenha que ser outro tipo.
        long cpf = 98765432109L; // Precisa terminar com "L" para dizer que é do tipo long. se começar com zero, talvez tenha que ser outro tipo.
        float pi = 3.14F; // Deve-se colocar "F" no final para diferencia-la do double.
        double salario = 1275.33;
    }
}